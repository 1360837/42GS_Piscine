#include <unistd.h>

void	ft_print_comb(void)
{
	char	h;
	char	t;
	char	o;

	h = '0';
	while (h <= '7')
	{
		t = h + 1;
		while (t <= '8')
		{
			o = t + 1;
			while (o <= '9')
			{
				write(1, &h, 1);
				write(1, &t, 1);
				write(1, &o, 1);
				if (h != '7' || t != '8' || o != '9')
					write(1, ", ", 2);
				o++;
			}
			t++;
		}
		h++;
	}
}
