int	ft_sqrt(int nb)
{
	int	x;
	int	x_;
	int	cnt;

	x = nb / 2;
	x_ = 0;
	cnt = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (x != x_ && cnt < 100)
	{
		x_ = x;
		x = (nb / x_ + x_) / 2;
		cnt++;
	}
	if (x * x != nb)
		return (0);
	return (x_);
}
